// Igual e diferente.
// = é atribuição
// == é para comparar o valor igual // != Diferente
// === compara valor e formato // !== Formato e valor diferente

let numero = "1"
console.log(numero == 1)
console.log(numero != 1)
console.log(numero === 1)
console.log(numero !== 1)

let resultado = numero == 1 // Atribuindo à variavel resultado o valor da comparação. True ou False
console.log(resultado)